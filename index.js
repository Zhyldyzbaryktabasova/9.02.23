function countup(a, b) {
  let ot = a;
  while (ot <= b) {
    console.log(ot)
    ot++;
  }
}
countup(5,10)
countup(-10,10)


function countdown(a, b){
  let ot = a;
  while (ot >= b) {
    console.log(ot)
    ot--;
  }
}
countdown(10, 5);
countdown(10, -10)